/// Represents the reason for migration
enum MigrationReason {
  /// Work-related migration
  work,
  
  /// Study-related migration
  study,
  
  /// Family-related migration
  family,
  
  /// Refugee or asylum seeker
  refugee,
  
  /// Retirement migration
  retirement,
  
  /// Investment-based migration
  investment,
  
  /// Lifestyle migration
  lifestyle,
  
  /// Other reasons
  other,
}
